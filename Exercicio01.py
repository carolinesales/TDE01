# Faça um algoritmo que leia um número inteiro e escreva seu sucessor e seu antecessor.

numeroInteiro = int(input("Digite um número inteiro: "))

numeroSucessor = numeroInteiro + 1
numeroAntecessor = numeroInteiro - 1

print("O número", numeroInteiro, "tem como seu sucessor o número", numeroSucessor, "e como antecessor o número", numeroAntecessor)